time = int(input('Введите время в минутах: '))

# КОММЕНТАРИЙ: когда возможно, следует использовать меньшее количество операций
# ИСПРАВИТЬ: в данном случае, вместо трёх арифметических операций можно использовать одну — взятие остатка от деления
print(f'{time} минут - это {time // 60} час {time % 60} мин')

# 514 минут - это 8 час 34 мин


